//4. Demonstrate relational operators with input values.
public class RelationalOperatorsDemo {
    public static void main(String[] args) {

        int a = 10;
        int b = 20;

        System.out.println("a = " + a);
        System.out.println("b = " + b);

        // Relational operator checks
        System.out.println("\na == b : " + (a == b));   // Equal to
        System.out.println("a != b : " + (a != b));     // Not equal to
        System.out.println("a > b  : " + (a > b));      // Greater than
        System.out.println("a < b  : " + (a < b));      // Less than
        System.out.println("a >= b : " + (a >= b));     // Greater than or equal
        System.out.println("a <= b : " + (a <= b));     // Less than or equal
    }
}
